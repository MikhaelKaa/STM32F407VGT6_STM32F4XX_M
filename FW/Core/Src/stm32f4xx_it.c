void HardFault_Handler_c(unsigned int* hardfault_args)
{
  volatile unsigned int stacked_r0;
  volatile unsigned int stacked_r1;
  volatile unsigned int stacked_r2;
  volatile unsigned int stacked_r3;
  volatile unsigned int stacked_r12;
  volatile unsigned int stacked_lr;
  volatile unsigned int stacked_pc;
  volatile unsigned int stacked_psr;

  stacked_r0 = ((unsigned long) hardfault_args[0]);
  stacked_r1 = ((unsigned long) hardfault_args[1]);
  stacked_r2 = ((unsigned long) hardfault_args[2]);
  stacked_r3 = ((unsigned long) hardfault_args[3]);

  stacked_r12 = ((unsigned long) hardfault_args[4]);
  stacked_lr  = ((unsigned long) hardfault_args[5]);
  stacked_pc  = ((unsigned long) hardfault_args[6]);
  stacked_psr = ((unsigned long) hardfault_args[7]);
  
  // Читаем регистры статуса
  volatile uint32_t cfsr  = SCB->CFSR;    // Configurable Fault Status Register
  volatile uint32_t hfsr  = SCB->HFSR;    // Hard Fault Status Register
  volatile uint32_t mmfar = SCB->MMFAR;  // MemManage Fault Address
  volatile uint32_t bfar  = SCB->BFAR;    // Bus Fault Address

  printf("[Hard fault handler]\r\n");
  printf("R0  = 0x%08x\r\n", stacked_r0);
  printf("R1  = 0x%08x\r\n", stacked_r1);
  printf("R2  = 0x%08x\r\n", stacked_r2);
  printf("R3  = 0x%08x\r\n", stacked_r3);
  printf("R12 = 0x%08x\r\n", stacked_r12);
  printf("LR  = 0x%08x\r\n", stacked_lr);
  printf("PC  = 0x%08x\r\n", stacked_pc);
  printf("PSR = 0x%08x\r\n", stacked_psr);

  printf("CFSR  = 0x%08lx\r\n", cfsr);
  printf("HFSR  = 0x%08lx\r\n", hfsr);
  printf("MMFAR = 0x%08lx\r\n", mmfar);
  printf("BFAR  = 0x%08lx\r\n", bfar);

  /* Go to infinite loop when Hard Fault exception occurs */
  while (1) {
    // Скорее всего это не сработает.
    // printf_flush();
  }
}

