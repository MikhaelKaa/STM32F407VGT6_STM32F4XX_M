/**
  * @brief This function handles Hard fault interrupt.
  */
void HardFault_Handler(void)
{
  /* USER CODE BEGIN HardFault_IRQn 0 */
  __asm volatile (
    "tst lr, #4              \n" // Проверяем, какой стек используется (MSP/PSP)
    "ite eq                   \n"
    "mrseq r0, msp            \n" // Если MSP, сохраняем в R0
    "mrsne r0, psp            \n" // Если PSP, сохраняем в R0
    "ldr r1, [r0, #24]        \n" // Загружаем PC из стека
    "b HardFault_Handler_c     \n" // Переходим в C-обработчик
  );
  /* USER CODE END HardFault_IRQn 0 */
  while (1)
  {
    /* USER CODE BEGIN W1_HardFault_IRQn 0 */
    /* USER CODE END W1_HardFault_IRQn 0 */
  }
}

